-- 查询验证
SELECT '=== 用户账号（API认证使用）===' AS info;
SELECT id, username, real_name, email, role, status FROM users WHERE is_deleted = 0;

SELECT '=== 区域字典（前10条）===' AS info;
SELECT id, region_code, region_name, region_level, parent_code FROM region_dictionary WHERE is_deleted = 0 LIMIT 10;

SELECT '=== 首页统计数据 ===' AS info;
SELECT * FROM v_homepage_statistics;

SELECT '=== 公告统计 ===' AS info;
SELECT
  business_type AS '业务类型',
  COUNT(*) AS '公告数量'
FROM announcements
WHERE is_deleted = 0 AND status = 1
GROUP BY business_type;

SELECT '=== 信息发布统计 ===' AS info;
SELECT
  type AS '信息类型',
  COUNT(*) AS '发布数量'
FROM info_publications
WHERE is_deleted = 0 AND status = 1
GROUP BY type;

SELECT '=== 业务范围 ===' AS info;
SELECT id, name, sort_order FROM business_scope WHERE is_deleted = 0 ORDER BY sort_order;

SELECT '=== 友情链接 ===' AS info;
SELECT id, name, url FROM friendly_links WHERE is_deleted = 0 AND status = 1 ORDER BY sort_order;
